/**
 * 根据名称查找符号详细信息
 * @param {string} name - 符号名称
 * @returns {object|null} 符号详细信息
 */
function findSymbolByName(name) {
  const allSymbols = [...JIU_XING_DATA, ...BA_MEN_DATA, ...BA_SHEN_DATA];
  return allSymbols.find(s => s.name === name) || null;
}

/**
 * 根据分类获取符号列表
 * @param {string} category - 分类名称（九星/八门/八神）
 * @returns {Array} 符号列表
 */
function getSymbolsByCategory(category) {
  const allSymbols = [...JIU_XING_DATA, ...BA_MEN_DATA, ...BA_SHEN_DATA];
  return allSymbols.filter(s => s.category === category);
}

/**
 * 根据宫位编号获取宫位详细信息
 * @param {number} palaceId - 宫位编号（1-9）
 * @returns {object|null} 宫位详细信息
 */
function getPalaceById(palaceId) {
  return JIU_GONG_DATA.find(g => g.number === palaceId) || null;
}

/**
 * 获取吉凶等级对应的样式类名
 * @param {string} luck - 吉凶等级
 * @returns {string} CSS类名
 */
function getLuckClass(luck) {
  const classMap = {
    '大吉': 'luck-great',
    '吉': 'luck-good',
    '小吉': 'luck-good',
    '平': 'luck-neutral',
    '小凶': 'luck-bad',
    '凶': 'luck-bad',
    '大凶': 'luck-terrible'
  };
  return classMap[luck] || 'luck-neutral';
}

